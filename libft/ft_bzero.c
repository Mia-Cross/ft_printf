#include <stddef.h>

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*c;

	i = 0;
	if (n == 0)
		return ;
	c = s;
	while (i < n)
	{
		c[i] = 0;
		i++;
	}
	return ;
}
