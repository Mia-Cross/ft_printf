#include <stdlib.h>

static int	ft_strlen(const char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}

static char	*ft_strcpy(char *dest, const char *src)
{
	int		i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char		*ft_strdup(const char *str)
{
	char	*copy;

	if (!(copy = (char *)malloc(sizeof(char) * ft_strlen(str) + 1)))
		return (copy);
	copy = ft_strcpy(copy, str);
	return (copy);
}
